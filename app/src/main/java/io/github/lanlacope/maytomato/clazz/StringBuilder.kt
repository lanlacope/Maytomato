/*
 * 実体参照可能か判定
 */
private fun isEntity(codePoint: Int): Boolean {
    return htmlEntityMap.any { it.key == codePoint }
}

/*
 * コードポイントを、可能なら実態文字参照に変換してappend
 */
fun StringBuilder.appendEntity(
    codePoint: Int
) {
    if (isEntity(codePoint)) append("&").append(htmlEntityMap[codePoint]).append(";")
    else appendCodePoint(codePoint)
}